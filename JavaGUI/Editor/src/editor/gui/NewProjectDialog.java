    public NewProjectDialog(JFrame frame, boolean isModal) {
        super(frame, isModal);
        initComponents();
        
        setLocationRelativeTo(frame);
        getRootPane().setDefaultButton(jButtonOk);
        jTextField_projName.requestFocusInWindow();
        jTextField_projName.addKeyListener(new KeyAdapter() {
            @Override
            public void keyReleased(KeyEvent ke) {
                if (ke.getKeyCode() == KeyEvent.VK_ESCAPE)
                    NewProjectDialog.this.setVisible(false);
            }
        });
        
        jCheckBoxGspn.setSelected(Util.getPreferences().getBoolean(START_GSPN_KEY, true));
        jCheckBoxDta.setSelected(Util.getPreferences().getBoolean(START_DTA_KEY, false));
        jCheckBoxMeasures.setSelected(Util.getPreferences().getBoolean(START_MEASURE_KEY, true));
        
        for (PetriNetType pnt : PetriNetType.values())
            jComboBoxPNType.addItem(pnt);
        try {
            PetriNetType defType = PetriNetType.valueOf(Util.getPreferences()
                    .get(START_PN_TYPE, PetriNetType.PT.name()));
            jComboBoxPNType.setSelectedItem(defType);
        }
        catch (IllegalArgumentException e) { }
        
        jComboBoxPNType.setEnabled(jCheckBoxGspn.isSelected());
        jCheckBoxMeasures.setEnabled(jCheckBoxGspn.isSelected());
    }

