    public enum PetriNetType {
        PT("PT", "Place/Transition Petri net"),
        CPN("CPN", "Colored (Well-Formed) Petri net"),
        GSPN("GSPN", "Generalized Stochastic Petri net"),
        SWN("SWN", "Stochastic Colored (Well-Formed) Petri net"),
        FullPN("PetriNet", "Full Petri net (all extensions)");
            
        String netPrefix, longDesc;
        private PetriNetType(String netPrefix, String longDesc) {
            this.netPrefix = netPrefix;
            this.longDesc = longDesc;
        }
        @Override
        public String toString() { return longDesc; }
    }

